package Day5;

public class ArithmeticOperation {
    static void main(String[] args) {
        int a= 17 , b = 5;
        System.out.println("a + b =" +(a+b));
        System.out.println("a -b = "+ (a-b));
        System.out.println("a * b = "+ (a*b));
        System.out.println("a / b = "+ (a/b));
        System.out.println("a % b = "+ (a%b));
    }
}
